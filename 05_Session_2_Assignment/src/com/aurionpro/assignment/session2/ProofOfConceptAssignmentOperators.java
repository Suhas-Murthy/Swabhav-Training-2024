package com.aurionpro.assignment.session2;

public class ProofOfConceptAssignmentOperators {
	public static void main(String[] args) {
		
		int number1 = 20;
		int number2 = 10;
		number1+=number2;
		System.out.println("number1 : "+ number1);
		
		number1 = 20;
		number2 = 10;
		number2-=number1;
		System.out.println("number1 : "+ number2);
		
		number1 = 20;
		number2 = 10;
		number1*=number2;	
		System.out.println("number1 : "+ number1);
		
		number1 = 20;
		number2 = 10;
		number1/=number2;	
		System.out.println("number1 : "+ number1);
		
		number1 = 20;
		number2 = 10;
		number1%=number2;	
		System.out.println("number1 : "+ number1);
	}
}
